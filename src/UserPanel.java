    private void addPlansAddBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addPlansAddBtnActionPerformed
        // TODO add your handling code here:
        try {
            int policyID = Integer.parseInt(policyIDField.getText());
            String policyHolderName = policyHolderNameField.getText();
            String carModel = carModelField.getText();
            CarType carType = CarType.valueOf(carTypeComboBox.getSelectedItem().toString());
            int manufacturingYear = Integer.parseInt(manufacturingYearField.getText());
            int carPrice = Integer.parseInt(carPriceField.getText());
            Car car = new Car(carModel, carType, manufacturingYear, carPrice);
            int numberOfClaims = Integer.parseInt(numberOfClaimsField.getText());
            int dateYear = Integer.parseInt(dateYearField.getText());
            int dateMonth = Integer.parseInt(dateMonthField.getText());
            int dateDay = Integer.parseInt(dateDayField.getText());
            MyDate date = new MyDate(dateYear, dateMonth, dateDay);
            String commentAndDriverAge = commentAndDriverAgeField.getText();
            InsurancePolicy policy;
            if(thirdPartyRadioButton.isSelected()) {
                policy = new ThirdPartyInsurancePolicy(policyID, policyHolderName, car, numberOfClaims, commentAndDriverAge, date);
            } else {
                int level = Integer.parseInt(levelField.getText());
                policy = new ComprehensiveInsurancePolicy( policyID, policyHolderName, car, numberOfClaims, Integer.parseInt(commentAndDriverAge), level, date);
            }
            if(this.user.addPolicy(policy)) {
                JOptionPane.showMessageDialog(this, "Policy added successfully!");
                policyIDField.setText("");
                policyHolderNameField.setText("");
                carModelField.setText("");
                manufacturingYearField.setText("");
                carPriceField.setText("");
                numberOfClaimsField.setText("");
                dateYearField.setText("");
                dateMonthField.setText("");
                dateDayField.setText("");
                commentAndDriverAgeField.setText("");
                levelField.setText("");
            } else {
                JOptionPane.showMessageDialog(this, "Adding policy failed!");
            }
        } catch(PolicyHolderNameException | NumberFormatException e) {
            JOptionPane.showMessageDialog(this, e.getMessage());
        }
        
    }//GEN-LAST:event_addPlansAddBtnActionPerformed

