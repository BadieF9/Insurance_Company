    public void fillTable() {
        planTable.removeAll();
        HashMap<Integer, InsurancePolicy> policies = this.user.getPolicies();
        for(int id : policies.keySet()) {
            InsurancePolicy policy = policies.get(id);
            String[] row = new String[13];
            row[0] = Integer.toString(policy.getPolicyID());
            row[1] = policy.getPolicyHolderName();
            Car car = policy.getCar();
            row[2] = car.getModel();
            row[3] = car.getType().toString();
            row[4] = Integer.toString(car.getManufacturingYear());
            row[5] = Double.toString(car.getPrice());
            row[6] = Integer.toString(policy.getNumberOfClaims());
            MyDate date = policy.getExpiryDate();
            row[7] = Integer.toString(date.getYear());
            row[8] = Integer.toString(date.getMonth());
            row[9] = Integer.toString(date.getDay());
            if(policy instanceof ThirdPartyInsurancePolicy) {
                row[10] = ((ThirdPartyInsurancePolicy) policy).getComments();
                row[11] = "NA";
                row[12] = "NA";
            } else {
                row[10] = "NA";
                row[11] = Integer.toString(((ComprehensiveInsurancePolicy) policy).getDriverAge());
                row[12] = Integer.toString(((ComprehensiveInsurancePolicy) policy).getLevel());
            }
            model.addRow(row);
        }
    }

